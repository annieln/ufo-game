bool play_again() {

  char replay;
  bool valid = false;

  while (!valid) {

    std::cout << "\nWould you like to play again? (y/n): ";
    std::cin >> replay;

    if (replay == 'y') {

      return true;

    } else if (replay == 'n') {

      return false;
      
    } else {

      std::cout << "Invalid Response. Please try again.";
      
    }

  }

}

